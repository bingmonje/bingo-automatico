create extension if not exists pg_net with schema extensions;

create table if not exists public.push_suscripciones (
  id bigint generated by default as identity primary key,
  endpoint text not null unique,
  p256dh text not null,
  auth text not null,
  usuario_id bigint references public.usuarios_bingo(id) on delete cascade,
  recibe_admin boolean not null default false,
  user_agent text,
  activo boolean not null default true,
  creado_en timestamptz not null default now(),
  actualizado_en timestamptz not null default now()
);

create index if not exists push_suscripciones_usuario_idx
  on public.push_suscripciones (usuario_id)
  where activo and usuario_id is not null;

create index if not exists push_suscripciones_admin_idx
  on public.push_suscripciones (recibe_admin)
  where activo and recibe_admin;

