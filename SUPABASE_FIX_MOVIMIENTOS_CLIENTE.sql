create or replace function public.bingo_cliente_entrar_sala_optimizada(
  p_token text,
  p_tab_id text
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_usuario_id integer;
  v_cedula text;
  v_actual public.bingo_sesiones_sala%rowtype;
  v_activos integer;
  v_limite constant integer := 80;
begin
  v_usuario_id := public.bingo_usuario_por_token(p_token);
  if v_usuario_id is null then
    return jsonb_build_object('ok', false, 'codigo', 'sesion_invalida');
  end if;
  if length(trim(coalesce(p_tab_id, ''))) < 12 then
    return jsonb_build_object('ok', false, 'codigo', 'tab_invalida');
  end if;

  select cedula into v_cedula
    from public.usuarios_bingo
   where id = v_usuario_id;

  if not exists (
    select 1
      from public.cartones_vendidos cv
     where cv.comprador_id = v_usuario_id
        or regexp_replace(coalesce(cv.cedula, ''), '\\D', '', 'g') =
           regexp_replace(coalesce(v_cedula, ''), '\\D', '', 'g')
  ) then
    return jsonb_build_object('ok', false, 'codigo', 'sin_cartones');
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtext('bingo-capacidad-sala-v1')::bigint
  );

  delete from public.bingo_sesiones_sala where expira_en <= now();

  select * into v_actual
    from public.bingo_sesiones_sala
   where usuario_id = v_usuario_id
   for update;

  if found and v_actual.tab_id <> p_tab_id and v_actual.expira_en > now() then
    return jsonb_build_object(
      'ok', false, 'codigo', 'cuenta_ya_conectada',
      'reintentar_en', greatest(1, ceil(extract(epoch from (v_actual.expira_en - now())))::integer)
    );
  end if;

  if not found then
    select count(*)::integer into v_activos
      from public.bingo_sesiones_sala
     where expira_en > now();
    if v_activos >= v_limite then
      return jsonb_build_object('ok', false, 'codigo', 'sala_llena', 'limite', v_limite);
    end if;
  end if;

  insert into public.bingo_sesiones_sala (
    usuario_id, tab_id, conectado_en, ultima_senal_en, expira_en
  ) values (
    v_usuario_id, p_tab_id, now(), now(), now() + interval '75 seconds'
  )
  on conflict (usuario_id) do update
    set tab_id = excluded.tab_id,
        ultima_senal_en = excluded.ultima_senal_en,
        expira_en = excluded.expira_en;

  select count(*)::integer into v_activos
    from public.bingo_sesiones_sala
   where expira_en > now();

  return jsonb_build_object('ok', true, 'activos', v_activos, 'limite', v_limite);
end;
$$;

create or replace function public.bingo_cliente_latido_sala_optimizada(
  p_token text,
  p_tab_id text
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_usuario_id integer;
begin
